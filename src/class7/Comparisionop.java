package class7;

public class Comparisionop {
    public static void main(String[] args) {
        int a=10;
        int t=20;
        System.out.println(a==t);
        System.out.println(a!=t);
        System.out.println(a>t);
        System.out.println(a<t);
        System.out.println(a>=t);
        System.out.println(a<=t);

    }
}
