package class7;

public class Arithmaticoprators {
    public static void main(String[] args) {

        int a=29;
        int b=8;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++ + ++a);
        System.out.println(b++ + b++);
        System.out.println(a-- - --a);
        System.out.println(b-- - --b);

    }
}
